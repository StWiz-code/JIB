#      2. 로컬 .env 파일 (python-dotenv)
#      3. 시스템 환경변수
# ──────────────────────────────────────────────────────────────────────────────
def _load_env() -> dict:
    """
    환경변수 로드 우선순위:
    1. Streamlit Cloud st.secrets
    2. 로컬 .env 파일
    3. 시스템 환경변수
    """
    keys = [
        'OPENAI_API_KEY',
        'ANTHROPIC_API_KEY',
        'PUBLIC_DATA_API_KEY',
        'WORKNET_API_KEY',
    ]

    # 1순위: Streamlit secrets (배포 환경)
    try:
        import streamlit as st
        if st.secrets and any(k in st.secrets for k in keys):
            return {k: st.secrets.get(k, '') for k in keys}
    except Exception:
        pass

    # 2순위: .env 파일 (로컬 환경)
    try:
        from dotenv import load_dotenv
        load_dotenv()
    except ImportError:
        pass

    # 3순위: 시스템 환경변수
    return {k: os.getenv(k, '') for k in keys}


