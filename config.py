# 10. 설정값 검증 함수
# ──────────────────────────────────────────────────────────────────────────────
# 가중치 합계 검증 시 허용할 부동소수점 오차
WEIGHT_SUM_TOLERANCE: float = 1e-6


def validate_config() -> Dict[str, object]:
    """
    필수 설정값을 검증한다.

    검증 항목:
        1) 필수 API 키가 .env에 존재하는지 확인한다.
        2) 다중지표 가중치 합계가 1.0(±허용 오차)인지 확인한다.

    Returns:
        dict:
            - "ok" (bool): 모든 검증을 통과하면 True.
            - "missing" (list[str]): 누락된 환경 변수 이름 목록.
            - "present" (list[str]): 정상적으로 로드된 환경 변수 이름 목록.
            - "weights" (dict):
                * "sum" (float): 현재 가중치 합계.
                * "expected" (float): 기대 합계 (1.0).
                * "tolerance" (float): 허용 오차.
                * "ok" (bool): 합계가 1.0(±허용 오차)이면 True.
                * "values" (dict[str, float]): 개별 가중치 값.
    """
    required: Dict[str, str] = {
        "OPENAI_API_KEY": OPENAI_API_KEY,
        "ANTHROPIC_API_KEY": ANTHROPIC_API_KEY,
        "PUBLIC_DATA_API_KEY": PUBLIC_DATA_API_KEY,
        "WORKNET_API_KEY": WORKNET_API_KEY,
    }

    missing: List[str] = [name for name, value in required.items() if not value]
    present: List[str] = [name for name, value in required.items() if value]

    weight_values: Dict[str, float] = {
        "WEIGHT_SIMILARITY": WEIGHT_SIMILARITY,
        "WEIGHT_DEMAND": WEIGHT_DEMAND,
        "WEIGHT_WAGE": WEIGHT_WAGE,
    }
    weight_sum: float = sum(weight_values.values())
    weight_ok: bool = abs(weight_sum - 1.0) <= WEIGHT_SUM_TOLERANCE

    return {
        "ok": len(missing) == 0 and weight_ok,
        "missing": missing,
        "present": present,
        "weights": {
            "sum": weight_sum,
            "expected": 1.0,
            "tolerance": WEIGHT_SUM_TOLERANCE,
            "ok": weight_ok,
            "values": weight_values,
        },
    }


